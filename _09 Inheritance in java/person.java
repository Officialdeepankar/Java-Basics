public class person {
    // "MULTIPLE INHERITANCE IS NOT POSSIBLE"
    private int age;
    private String name;

    public void setage(int a)
    {
          age =a;
    }

    public void setname(String n)
    {
        name=n;
    }

    public String getname()
    {
        return name;
    }

    public int getage()
    {
        return age;
    }

    
}